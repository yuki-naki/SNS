// ポップアップscript
//createGroupポップアップ
$(function() {

	$('#groupIcon').click(function(e) {// show popupボタンクリック時の処理
	    $('#createGroupPopup, #createGroupLayer').show();
	});

	// レイヤー/ポップアップのcloseボタンクリック時の処理
	$('#closeCreateGroup, #layer').click(function(e) {
	    $('#createGroupPopup, #createGroupLayer').hide();
	});
});

